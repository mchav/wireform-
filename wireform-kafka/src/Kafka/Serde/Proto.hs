A typed 'Serde' value for any @wireform-proto@-derived message,
suitable for plugging into 'Kafka.Topic.Topic' or directly into
'Kafka.Client.Producer.publish' \/ a typed consume helper.

= Recommended usage

@
import           Data.Text                  (Text)
import qualified Kafka
import qualified Kafka.Topic                as Topic
import qualified Kafka.Serde                as Serde
import qualified Kafka.Serde.Proto          as ProtoSerde

import qualified My.Proto.Generated.Events as Pb  -- @wireform-proto@-generated module

events :: Topic.'Topic.Topic' Text Pb.OrderPlaced
events = Topic.'Topic.topic' \"orders\" Serde.'Serde.textSerde' ProtoSerde.'protoSerde'

main =
  Kafka.'Kafka.withProducer' [\"localhost:9092\"] Kafka.'Kafka.defaultProducerConfig' $ \\p ->
    Kafka.'Kafka.publish' p events (Just \"order-42\") order
@

The 'protoSerde' value is fully generic over any
'Proto.Encode.MessageEncode' \/ 'Proto.Decode.MessageDecode' pair
— that is, any message type generated by the @wireform-proto@
code generator or hand-written with the appropriate instances.

= Wire format

The serialised bytes are __just the message body__ — no length
prefix, no Schema Registry envelope, no magic byte. Length
framing is the caller's responsibility (Kafka itself already
records the record's value length in the record batch header, so
in practice you don't need any).

If you need the Confluent Schema Registry envelope (the
\"@magic byte | 4-byte schema id | message bytes@\" framing the
JVM @KafkaProtobufSerializer@ produces), wrap 'protoSerde' with
the helpers from "Kafka.Streams.Serde.SchemaRegistry".
-}
module Kafka.Serde.Proto
  ( -- * Generic message serde
    protoSerde
    -- * Lower-level access
  , encodeProto
  , decodeProto
  ) where

import qualified Data.ByteString        as BS
import           Data.Bifunctor         (first)

import           Kafka.Serde            (Serde, unsafeSerde)

import qualified Proto.Encode           as Proto.Encode
import qualified Proto.Decode           as Proto.Decode

-- | Bidirectional protobuf serde. Encodes the message body via
-- 'Proto.Encode.encodeMessage' (no length prefix); decodes via
-- 'Proto.Decode.decodeMessage' and surfaces 'DecodeError' as the
-- 'Serde'\'s 'String'-typed error channel.
--
-- The encode path is a single pass over the message structure
-- (the @wireform-proto@ codegen emits direct @Builder@ ops); the
-- decode path is also single-pass and never copies the input
-- bytes, so the serde adds no Kafka-specific overhead beyond
-- whatever the generated codec already does.
protoSerde
  :: (Proto.Encode.MessageEncode a, Proto.Decode.MessageDecode a)
  => Serde a
protoSerde = unsafeSerde encodeProto decodeProto

-- | Standalone encoder; calls 'Proto.Encode.encodeMessage'.
encodeProto :: Proto.Encode.MessageEncode a => a -> BS.ByteString
encodeProto = Proto.Encode.encodeMessage

-- | Standalone decoder. Threads the @wireform-proto@ 'DecodeError'
-- through 'show' so it fits the 'Serde'\'s 'Left' 'String' shape;
-- callers that want the structured error should call
-- 'Proto.Decode.decodeMessage' directly.
decodeProto
  :: Proto.Decode.MessageDecode a
  => BS.ByteString
  -> Either String a
decodeProto = first show . Proto.Decode.decodeMessage
