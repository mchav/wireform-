{-|
Module      : Main
Description : Generate test-vectors.json from in-tree encoders

A standalone executable that walks a curated set of @(message_type,
version)@ pairs, builds a representative value for each, encodes
it with the in-tree generator, and emits a JSON file in the format
expected by 'Protocol.Generated.ComprehensiveSpec' /
'Protocol.Generated.KnownGoodSpec':

@
[
  { "api_key":     18
  , "message_type":"ApiVersionsRequest"
  , "version":      3
  , "test_case":   "default"
  , "description": "ApiVersionsRequest v3 (default fields)"
  , "hex":         "..."
  },
  ...
]
@

The vectors here are generated by /our/ encoder, so they primarily
serve as a *byte-level regression* baseline: the comprehensive
round-trip spec checks that `decode → encode` reproduces the same
bytes, and any change to the encoder output for these messages
breaks the test until somebody explicitly accepts the new bytes.

When we eventually have an external (Java / Rust / @librdkafka@) test
fixture, it slots in alongside these — same JSON shape.

Usage:

> cabal run wireform-kafka:exe:gen-test-vectors > test-vectors.json
-}
