-- | A test vector from the Rust generator
data TestVector = TestVector
  { messageType :: Text
  , version :: Int16
  , description :: Text
  , hex :: Text
  } deriving (Show, Generic)

instance Aeson.FromJSON TestVector where
  parseJSON = Aeson.withObject "TestVector" $ \v -> TestVector
    <$> v Aeson..: "message_type"
    <*> v Aeson..: "version"
    <*> v Aeson..: "description"
    <*> v Aeson..: "hex"

-- | Load test vectors from JSON file. Returns an empty list if the
-- vectors aren't present so the suite can still run in environments
-- where the fixture hasn't been provisioned.
--
-- Looks for the file in two places, in order:
--
--   1. @test-data\/test-vectors.json@ relative to CWD when @cabal test@
--      runs from the package root (the vendored snapshot generated by
--      @cabal run wireform-kafka:exe:gen-test-vectors@).
--   2. @test-vectors.json@ in CWD (legacy: where the original
--      Rust generator dropped its output).
loadTestVectors :: IO [TestVector]
loadTestVectors = do
  candidate <- pickFirstExisting
    [ "wireform-kafka/test-data/test-vectors.json"  -- run from workspace root
    , "test-data/test-vectors.json"                 -- run from package root
    , "test-vectors.json"                           -- legacy
    ]
  case candidate of
    Nothing -> pure []
    Just p -> do
      content <- BL.readFile p
      case Aeson.eitherDecode content of
        Left err -> error $ "Failed to parse test vectors at " <> p <> ": " <> err
        Right vectors -> return vectors
  where
    pickFirstExisting [] = pure Nothing
    pickFirstExisting (p:ps) = do
      ok <- Dir.doesFileExist p
      if ok then pure (Just p) else pickFirstExisting ps

-- | Convert hex string to ByteString
hexToBS :: Text -> Either String BS.ByteString
hexToBS hexText = 
  let hexStr = show hexText -- Convert Text to String
      pairs = takeWhile (not . null) $ map (take 2) $ iterate (drop 2) (drop 1 $ init hexStr)
      parseHex [c1, c2] = do
        d1 <- hexDigit c1
        d2 <- hexDigit c2
        return $ fromIntegral (d1 * 16 + d2)
      parseHex _ = Left "Invalid hex pair"
      hexDigit c
        | c >= '0' && c <= '9' = Right (fromEnum c - fromEnum '0')
        | c >= 'a' && c <= 'f' = Right (fromEnum c - fromEnum 'a' + 10)
        | c >= 'A' && c <= 'F' = Right (fromEnum c - fromEnum 'A' + 10)
        | otherwise = Left $ "Invalid hex digit: " ++ [c]
  in BS.pack <$> mapM parseHex pairs

-- | Test a single MetadataRequest vector through the WireCodec
-- instance: decode the bytes, re-encode, and assert they match.
testMetadataRequest :: TestVector -> Spec
testMetadataRequest vec = it (show (description vec)) $ do
  bytes <- case hexToBS (hex vec) of
    Left err -> expectationFailure $ "Failed to parse hex: " ++ err
    Right bs -> return bs

  case WC.runDecodeVer @MetadataRequest (version vec) bytes of
    Left err  -> expectationFailure $ "Decode failed: " ++ err
    Right msg -> do
      let reencoded = WC.runEncodeVer @MetadataRequest (version vec) msg
      reencoded `shouldBe` bytes

-- | Create test tree from vectors
createTests :: [TestVector] -> Spec
createTests vectors =
  let metadataVectors = filter (\v -> messageType v == "MetadataRequest") vectors
      metadataGroup
        | null metadataVectors =
            it "MetadataRequest (skipped: no test-vectors.json)" $ (pure () :: IO ())
        | otherwise =
            describe "MetadataRequest" (mapM_ testMetadataRequest metadataVectors)
  in describe "Known-Good Test Vectors" $ sequence_ [ metadataGroup ]

tests :: IO Spec
tests = do
  vectors <- loadTestVectors
  return $ createTests vectors

