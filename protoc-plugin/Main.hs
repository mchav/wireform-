-- | protoc plugin for hs-proto.
--
-- Usage: protoc --plugin=protoc-gen-hs-proto=./protoc-gen-hs-proto --hs-proto_out=gen/ foo.proto
module Main where

import qualified Data.Text as T
import qualified Data.Vector as V

import Proto.Google.Protobuf.Compiler.Plugin
import Proto.Google.Protobuf.Descriptor

main :: IO ()
main = pluginMain handleRequest

handleRequest :: CodeGeneratorRequest -> IO CodeGeneratorResponse
handleRequest req = do
  let files = V.toList (cgrFileToGenerate req)
      protos = V.toList (cgrProtoFile req)
      outputFiles = concatMap (generateForFile files) protos
  pure defaultCodeGeneratorResponse
    { cgrsFile = V.fromList outputFiles
    , cgrsSupportedFeatures = 1
    }

generateForFile :: [T.Text] -> FileDescriptorProto -> [CodeGeneratorResponseFile]
generateForFile requestedFiles fdp =
  if fdpName fdp `elem` requestedFiles
  then
    let modulePath = T.replace "." "/" (fdpPackage fdp)
        fileName = modulePath <> "/" <> T.replace "." "/" (fdpName fdp) <> ".hs"
        content = "-- Generated by protoc-gen-hs-proto from " <> fdpName fdp <> "\n"
                  <> "-- Package: " <> fdpPackage fdp <> "\n"
                  <> "module Proto.Gen." <> T.replace "/" "." modulePath <> " where\n\n"
                  <> "-- TODO: full code generation from FileDescriptorProto\n"
    in [CodeGeneratorResponseFile fileName content]
  else []
